package Aula_12;

public class TrabalhandoComEstruturasDeRepeticao_05 {

	public static void main(String[] args) {
		int num = 0;
		
		do {
			System.out.println("Contando: " + num);
			num++;
		} while (num <=5);
// executa as mesmas ações do while, só que primeiro executando e depois verificando
// garante que o programa seja executado ao menos uma vez, ex. se o numero inserido for 5
	}

}
