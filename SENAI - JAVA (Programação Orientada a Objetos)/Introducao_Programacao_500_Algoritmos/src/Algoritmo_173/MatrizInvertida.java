package Algoritmo_173;

public class MatrizInvertida {

	public static void main(String[] args) {
		System.out.println("Algoritmo: Imprime a matriz 0 x 9 com posições invertidas.\n");
		
		for (int i = 0; i <= 9; i++) {
			
			for (int j = 0; j <= 9 ; j++) {
				System.out.print(i + "-" + j + "  " );
			}
			
			System.out.print("\n");
		}
		
	}

}
