package Algoritmo_166;

public class MatrizDezPorDez {

	public static void main(String[] args) {
		System.out.println("Algoritmo: Imprimi uma matriz 10 x 10.\n");
		
		for (int i = 1; i <=10 ; i++) {
			
			for (int j = 1; j <= 10 ; j++) {
				System.out.print(i + "-" + j + "  ");
			}
			
			System.out.println("\n");
		}
	}

}
