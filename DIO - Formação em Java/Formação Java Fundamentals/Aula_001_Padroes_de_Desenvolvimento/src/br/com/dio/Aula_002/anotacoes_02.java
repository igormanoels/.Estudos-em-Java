package br.com.dio.Aula_002;

public class anotacoes_02 {
	public static void main(String[] args) {
		
		int op = 3;
		
		// Escola...Caso
		switch (op) {
		case 1:
			System.out.println("Primeira saída.");

			break;
		case 2:
			System.out.println("Segunda saída.");
			
			break;
		case 3:
			System.out.println("Terceira saída.");
			
			break;
		case 4:
			System.out.println("Quarta saída.");
			
			break;

		default:
			System.out.println("Nenhuma opção foi escolhida.");

			break;
		}
	}
}
