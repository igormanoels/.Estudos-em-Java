package Aula_13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		// COMPARADORES RETORNAM VALORES BOLEANOS
		int valor1 = 1;
		int valor2 = 2;
		
		// Igualdade
		System.out.println(valor1 == valor2);
		
		// Diferente
		System.out.println(valor1 != valor2);

		// É maior
		System.out.println(valor1 > valor2);

		// É Menor
		System.out.println(valor1 < valor2);

		// É maior ou igual
		System.out.println(valor1 >= valor2);

		// É menor ou igual
		System.out.println(valor1 <= valor2);
	
	}
	
}

