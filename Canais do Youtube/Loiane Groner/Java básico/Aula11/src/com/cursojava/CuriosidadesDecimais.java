package com.cursojava;

public class CuriosidadesDecimais {
	
	public static void main(String[] args) {
		
		double d1 = 123.5;
		System.out.println(d1);
		
		// Os valores decimais também aceitam valores de notações científicas
		// para esse caso temos 1.23 elevado a 2
		double d2 = 1.23e2;		
		System.out.println(d2);
		
	}

}
